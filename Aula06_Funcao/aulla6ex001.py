#  e que forneça a soma desses três argumentos.

def soma(x1, y1, z1):
    soma = x1 + y1 + z1
    print(f'O resultadfo da sua soma é : {soma}')

x2 = int(input(' Esacreva o primeiro número: '))
y2 = int(input('Escrava o segundo número: '))
z2 = int(input('Escreva o terceiro número'))
soma(x2, y2, z2)


